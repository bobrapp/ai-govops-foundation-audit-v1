
-- ============= AOS Versions & Control Catalog =============
CREATE TABLE IF NOT EXISTS public.aos_versions (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  version text NOT NULL UNIQUE,
  status text NOT NULL DEFAULT 'draft',  -- draft | active | retired
  notes text,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.aos_controls (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  version_id uuid NOT NULL REFERENCES public.aos_versions(id) ON DELETE CASCADE,
  control_id text NOT NULL,           -- e.g. AOS-L1-01
  level int NOT NULL DEFAULT 1,
  domain text NOT NULL,               -- pipeline | evidence | decisioning | safety | data | model | ops
  objective text NOT NULL,            -- what the control achieves
  testing_procedure text NOT NULL,    -- how an assessor tests it
  evidence_expected text NOT NULL,    -- what evidence the pipeline must produce
  framework_refs text[] NOT NULL DEFAULT '{}',
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (version_id, control_id)
);

ALTER TABLE public.aos_versions ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.aos_controls ENABLE ROW LEVEL SECURITY;

-- Public catalog: anyone can read
CREATE POLICY "aos_versions readable by all"
  ON public.aos_versions FOR SELECT TO anon, authenticated USING (true);
CREATE POLICY "aos_controls readable by all"
  ON public.aos_controls FOR SELECT TO anon, authenticated USING (true);
-- Only admins manage
CREATE POLICY "admins manage aos_versions"
  ON public.aos_versions FOR ALL TO authenticated
  USING (public.has_role(auth.uid(),'admin'))
  WITH CHECK (public.has_role(auth.uid(),'admin'));
CREATE POLICY "admins manage aos_controls"
  ON public.aos_controls FOR ALL TO authenticated
  USING (public.has_role(auth.uid(),'admin'))
  WITH CHECK (public.has_role(auth.uid(),'admin'));

-- Helper to get active version id
CREATE OR REPLACE FUNCTION public.aos_active_version()
RETURNS uuid LANGUAGE sql STABLE SECURITY DEFINER SET search_path = public AS $$
  SELECT id FROM public.aos_versions WHERE status='active' ORDER BY created_at DESC LIMIT 1
$$;

