-- ============= Engagements (assessor ↔ review with independence) =============
CREATE TABLE IF NOT EXISTS public.assessor_engagements (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  review_id uuid NOT NULL REFERENCES public.reviews(id) ON DELETE CASCADE,
  assessor_id uuid NOT NULL REFERENCES public.qaga_assessors(id) ON DELETE RESTRICT,
  firm_id uuid NOT NULL REFERENCES public.qagac_firms(id) ON DELETE RESTRICT,
  status text NOT NULL DEFAULT 'requested', -- requested | declared | active | recused | completed
  client_org text NOT NULL,           -- the submitter's organization (snapshotted)
  independence_declared_at timestamptz,
  independence_signed_by uuid,        -- assessor user_id who signed
  independence_attestation text,      -- the text they affirmed
  conflict_reason text,               -- if recused
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (review_id, assessor_id)
);
CREATE TRIGGER trg_engagements_touch BEFORE UPDATE ON public.assessor_engagements
  FOR EACH ROW EXECUTE FUNCTION public.touch_updated_at();
ALTER TABLE public.assessor_engagements ENABLE ROW LEVEL SECURITY;

-- Submitter sees engagements on their own reviews; assessor sees own; admin sees all
CREATE POLICY "engagements visibility"
  ON public.assessor_engagements FOR SELECT TO authenticated
  USING (
    public.has_role(auth.uid(),'admin')
    OR EXISTS (SELECT 1 FROM public.qaga_assessors a WHERE a.id = assessor_engagements.assessor_id AND a.user_id = auth.uid())
    OR EXISTS (SELECT 1 FROM public.reviews r WHERE r.id = assessor_engagements.review_id AND r.submitter_id = auth.uid())
  );
CREATE POLICY "engagements admin manage"
  ON public.assessor_engagements FOR ALL TO authenticated
  USING (public.has_role(auth.uid(),'admin'))
  WITH CHECK (public.has_role(auth.uid(),'admin'));

-- ============= Engagement workflow functions =============

-- Request an engagement (admin assigns assessor to review).
-- Refuses if firm has a recorded dev engagement with the submitter org (Arthur Andersen rule).
CREATE OR REPLACE FUNCTION public.request_engagement(_review_id uuid, _assessor_id uuid)
RETURNS uuid LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE
  _firm uuid;
  _client text;
  _conflict int;
  _eng_id uuid;
BEGIN
  IF NOT public.has_role(auth.uid(),'admin') THEN
    RAISE EXCEPTION 'admin only';
  END IF;

  SELECT firm_id INTO _firm FROM public.qaga_assessors
    WHERE id = _assessor_id AND status='active' AND training_level='qaga';
  IF _firm IS NULL THEN RAISE EXCEPTION 'assessor not active QAGA or has no firm'; END IF;

  -- Snapshot submitter org
  SELECT COALESCE(p.organization,'(unknown)') INTO _client
    FROM public.reviews r
    LEFT JOIN public.profiles p ON p.id = r.submitter_id
    WHERE r.id = _review_id;
  IF _client IS NULL THEN RAISE EXCEPTION 'review not found'; END IF;

  -- Independence check
  SELECT COUNT(*) INTO _conflict FROM public.firm_dev_engagements
    WHERE firm_id = _firm AND client_org = _client
      AND (active_through IS NULL OR active_through > now() - interval '12 months');
  IF _conflict > 0 THEN
    RAISE EXCEPTION 'independence conflict: firm has development engagement with client_org=%', _client;
  END IF;

  INSERT INTO public.assessor_engagements (review_id, assessor_id, firm_id, status, client_org)
  VALUES (_review_id, _assessor_id, _firm, 'requested', _client)
  RETURNING id INTO _eng_id;
  RETURN _eng_id;
END $$;

-- Assessor signs the independence declaration to activate.
CREATE OR REPLACE FUNCTION public.sign_independence_declaration(_engagement_id uuid, _attestation text)
RETURNS void LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
DECLARE
  _user uuid := auth.uid();
  _ok boolean;
BEGIN
  SELECT EXISTS(SELECT 1 FROM public.assessor_engagements e
    JOIN public.qaga_assessors a ON a.id = e.assessor_id
    WHERE e.id = _engagement_id AND a.user_id = _user
  ) INTO _ok;
  IF NOT _ok THEN RAISE EXCEPTION 'not assigned to this engagement'; END IF;

  UPDATE public.assessor_engagements
  SET status = 'active',
      independence_declared_at = now(),
      independence_signed_by = _user,
      independence_attestation = _attestation
  WHERE id = _engagement_id;
END $$;

-- Maintain firm active assessor count
CREATE OR REPLACE FUNCTION public.recount_firm_assessors()
RETURNS trigger LANGUAGE plpgsql SECURITY DEFINER SET search_path = public AS $$
BEGIN
  UPDATE public.qagac_firms f SET active_assessor_count = (
    SELECT COUNT(*) FROM public.qaga_assessors a
    WHERE a.firm_id = f.id AND a.training_level='qaga' AND a.status='active'
  ) WHERE f.id = COALESCE(NEW.firm_id, OLD.firm_id);
  RETURN NEW;
END $$;
CREATE TRIGGER trg_recount_firm_assessors
  AFTER INSERT OR UPDATE OR DELETE ON public.qaga_assessors
  FOR EACH ROW EXECUTE FUNCTION public.recount_firm_assessors();
